    /**
     * 随机生成一个座机号码
     *
     * @param boolean $includeAreaCode 是否包含区号
     * @return string
     */
    public static function random($includeAreaCode = true)
    {
        $area = array_rand(self::$areaCode);
        $len = self::$areaCode[$area] - 1;

        $first = mt_rand(2, 8);

        if (self::$areaCode[$area] < 8) {
            $number = mt_rand(0, 999999);
        } else {
            $number = mt_rand(0, 9999999);
        }

        if ($includeAreaCode) {
            $result = '(' . $area . ')' . $first . str_pad($number, $len, '0', STR_PAD_LEFT);
        } else {
            $result = $first . str_pad($number, $len, '0', STR_PAD_LEFT);
        }

        return $result;
    }
